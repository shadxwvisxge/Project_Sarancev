#Дано целое число N (>0) и строка S. Преобразовать строку S в строку длины N следующим образом: если длина строки S больше N, то отбросить первые символы, если длина строки S меньше N, то в ее начало добавить символы «.» (точка).
# Ввод числа N и строки S
N = int(input("N: "))
S = input("S: ")
# Если длина строки больше N — оставить только последние N символов
if len(S) > N:
    S = S[-N:]
# Если длина строки меньше N — добавить в начало нужное количество точек
elif len(S) < N:
    S = '.' * (N - len(S)) + S
# Вывод полученной строки
print(S)