#Даны два списка A и B одинакового размера N. Сформировать новый список C того же размера, каждый элемент которого равен максимальному из элементов списков A и B.
#Ввод размера N
N = int(input("Введите размер списков: "))
#Ввод списка A
A = list(map(int, input("Введите список A через пробел: ").split()))
#Ввод списка B
B = list(map(int, input("Введите список B через пробел: ").split()))
#Формируем список C, выбирая максимум из A и B для каждого элемента
C = [max(A[i], B[i]) for i in range(N)]
#Вывод результата
print("Список C:", C)