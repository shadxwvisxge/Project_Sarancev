#Дан целочисленный список размера N. Проверить, чередуются ли в нем четные и нечетные числа. Если чередуются, то вывести 0, если нет, то вывести порядковый номер первого элемента, нарушающего закономерность.
#Вводим функцию для проверки чередований
def check_alternation(lst):
    for i in range(len(lst) - 1):
        if (lst[i] % 2) == (lst[i+1] % 2):
            #Найден первый элемент, нарушающий закономерность
            return i + 1  # порядковый номер (начиная с 1)
    return 0  # если чередуются
#Пример использования:
N = int(input("Введите размер списка: "))
lst = list(map(int, input("Введите элементы через пробел: ").split()))
result = check_alternation(lst)
print(result)