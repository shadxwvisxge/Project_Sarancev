import sqlite3

# Создаем или подключаемся к базе данных
conn = sqlite3.connect('polis.db')
cursor = conn.cursor()

# Создаем таблицу 'Пациент' с указанной структурой
cursor.execute('''
CREATE TABLE IF NOT EXISTS Пациент (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    ФИО_пациента TEXT NOT NULL,
    ФИО_врача TEXT NOT NULL,
    диагноз TEXT NOT NULL,
    стоимость_лечение REAL NOT NULL
)
''')

# Функция для добавления нового пациента
def добавить_пациента(фио_пациента, фио_врача, диагноз, стоимость):
    cursor.execute('''
    INSERT INTO Пациент (ФИО_пациента, ФИО_врача, диагноз, стоимость_лечение)
    VALUES (?, ?, ?, ?)
    ''', (фио_пациента, фио_врача, диагноз, стоимость))
    conn.commit()

# Функция для получения всех записей
def получить_пациентов():
    cursor.execute('SELECT * FROM Пациент')
    return cursor.fetchall()

# Пример использования
добавить_пациента('Иванов Иван Иванович', 'Петров Петр Петрович', 'Грипп', 1500.0)
добавить_пациента('Сидорова Мария Ивановна', 'Смирнов Алексей Владимирович', 'Ангина', 2500.0)

пациенты = получить_пациентов()
for п in пациенты:
    print(п)

# Закрываем соединение
conn.close()